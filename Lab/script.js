function tugas1(a, b) {
  return (a * b) / b;
}
console.log(tugas1(2, 5));
// tugas 2
function tugas2(a, b, c) {
  return a || b || c;
}
console.log(tugas2(false, false, true));

// tugas 3
function tugas3(x, y, z) {
  return x + " " + y + " " + z;
}
console.log(tugas3("Agung", "adi", "Rangga"));

